export const getProductById = (id) => {
  return products.find((product) => product.id === Number.parseInt(id))
}

export const getRelatedProducts = (id, limit = 4) => {
  const product = getProductById(id)
  if (!product) return []

  return products.filter((p) => p.id !== Number.parseInt(id) && p.category === product.category).slice(0, limit)
}

export const getFeaturedProducts = (limit = 4) => {
  return products.slice(0, limit)
}

export const getNewArrivals = (limit = 4) => {
  return products.filter((product) => product.isNew).slice(0, limit)
}

export const getSaleProducts = (limit = 4) => {
  return products.filter((product) => product.onSale).slice(0, limit)
}
